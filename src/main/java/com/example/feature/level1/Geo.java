package com.example.feature.level1;

public class Geo {
    private float lat;
    private float lng;

    // Constructor
    public Geo() {
    }

    // Getter
    public float getLat() {
        return this.lat;
    }

    public float getLng() {
        return this.lng;
    }

    // Setter
    public void setLat(float lat) {
        this.lat = lat;
    }

    public void setLng(float lng) {
        this.lng = lng;
    }
}
